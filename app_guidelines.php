<h2>VC4A Application Guidelines</h2>
<p><strong>So you want to make an app!</strong> That’s great. We’ve done our best to put everything you need to build something right here, from <a href="my_apps.php">app registration</a> to <a href="documentation.php">API documentation</a>. Terms of service for app development can be complex and difficult to understand, so we’ve provided some guidelines in the form of a handy hypothetical Q&amp;A below:</p>
<h4>What’s the VC4A API all about? What kind of app should I build?</h4>
<p>Wherever there are African ventures, there is a place for VC4Africa. VC4A is a reliable, flexible and powerful way register and share your venture with anyone on the web.
    If you’re building any kind of app that involves pushing African ventures-related content to users, or where your users might want to follow a venture they're interested in, the VC4A API is there for you.</p>
<h4>I want to call my app “VC4A for Android.” Is that okay?</h4>
<p>NOPE. You can’t call your app anything that would make people think that we made it or endorse it in any capacity. Frankly, we don’t know why you would want us to take credit for your work, anyhow!</p>
<h4>Ok, fine. How about “DeveloperCorp’s Awesome VC4A App.” Is that cool?</h4>
<p>SURE, BUT make sure that you note explicitly in your app’s description that “this application uses the VC4A API, but is neither developed nor endorsed by VC4AAfrica.”
    We don’t explicitly forbid the use of the word “VC4A” in the name of your app, but you need to be clear that you are not making an official app.</p>
<h4>I want to charge money for my app. is THAT cool?</h4>
<p>YES, BUT you need to be charging for the value you’re adding to the VC4A API, not for access to the API itself. For example, you can’t make an app that charges for each venture retrieved via the VC4A API.
    However, if you invest in making a beautiful, unique and useful app that uses the VC4A API for sharing ventures data in a unique way, you are absolutely welcome to charge money for it. If you’re unsure, just ask.</p>
<h4>Can the logo for my app include the VC4A logo?</h4>
<p>As long as it does not look like the official VC4A logo, sure. Also, this is obviously the least of your problems...</p>
